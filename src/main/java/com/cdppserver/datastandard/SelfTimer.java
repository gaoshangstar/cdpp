package com.cdppserver.datastandard;

public class SelfTimer {

	private String timerBeginTime;
	private String timerIntervalTime;
	private String timerUnit;
	private String timerRemark;
	public String getTimerBeginTime() {
		return timerBeginTime;
	}
	public void setTimerBeginTime(String timerBeginTime) {
		this.timerBeginTime = timerBeginTime;
	}
	public String getTimerIntervalTime() {
		return timerIntervalTime;
	}
	public void setTimerIntervalTime(String timerIntervalTime) {
		this.timerIntervalTime = timerIntervalTime;
	}
	public String getTimerUnit() {
		return timerUnit;
	}
	public void setTimerUnit(String timerUnit) {
		this.timerUnit = timerUnit;
	}
	public String getTimerRemark() {
		return timerRemark;
	}
	public void setTimerRemark(String timerRemark) {
		this.timerRemark = timerRemark;
	}
}
